#include "libFunc.h"


bool LibFuncs::isLibFunc(const std::string& funcName) 
{
    if (funcName.find ("llvm") == 0 || funcName.find ("mi_") == 0 || funcName.find ("_mi_") == 0)
    {
        return true;
    }

    return libFunctions.find(funcName) != libFunctions.end();
}

